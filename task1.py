#创建一个整数变量 age，赋值为你的年龄。
age=22
#创建一个浮点数变量 height，赋值为你的身高（例如：1.75）。
height=1.70
#创建一个字符串变量 name，赋值为你的名字。
name="rael"#字符串一定要加引号
#创建一个布尔变量 is_student，表示你是否是学生。
is_student=True#布尔值 true 和 false 在 Python 中是 True 和 False
#打印所有变量的值，并使用 type() 函数打印它们的类型。
# 【优化点】使用 f-string 让输出更整洁易读
print(f"变量 age 的值是: {age}，类型是: {type(age)}")
print(f"变量 height 的值是: {height}，类型是: {type(height)}")
print(f"变量 name 的值是: {name}，类型是: {type(name)}")
print(f"变量 is_student 的值是: {is_student}，类型是: {type(is_student)}")